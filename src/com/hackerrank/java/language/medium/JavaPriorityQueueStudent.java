package com.hackerrank.java.language.medium;

public class JavaPriorityQueueStudent {
    int id;
    String name;
    double cgpa;

    public JavaPriorityQueueStudent(int id, String name, double cgpa) {
        super();
        this.id = id;
        this.name = name;
        this.cgpa = cgpa;
    }
    public int getID() {
        return id;
    }
    public String getName() {
        return name;
    }
    public double getCgpa() { return cgpa; }
}
